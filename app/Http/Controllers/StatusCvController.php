    public function statuscvaccept($id)
    {
        $user = User::find($id);
        $user->status_cv = 1;
        $user->save();
        return redirect()->route('statuscv');
    }

    public function statuscvreject($id)
    {
        $user = User::find($id);
        $user->status_cv = 2;
        $user->save();
        return redirect()->route('statuscv');
